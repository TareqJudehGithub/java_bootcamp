public class BooleanComparison {
  /**
   * 
   * Boolean comparisons practice
   */
  public static void main(String[] args) {

    // int comparisons
    int chemistryGrade = 95;
    int biologyGrade = 75;

    System.out.println(chemistryGrade > biologyGrade);
    System.out.println(chemistryGrade < biologyGrade);

    System.out.println("");

    // double comparisons
    double sales = 45.55;
    double costs = 35.47;

    System.out.println(costs < sales);

    // Not equal to  !=
    System.out.println(sales != costs);


    // String comparisons
    System.out.println("");

    String name1 = "Ali";
    String name2 = "Muhammad";

    System.out.println(name1.equals(name2));

  // Equals to ==
  System.out.println("");

  System.out.println(54 == 60);
  System.out.println(54 == 54);
  }


  
}
