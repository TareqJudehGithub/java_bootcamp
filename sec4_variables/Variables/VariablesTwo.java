public class VariablesTwo {
  public static void main(String[] args) {
    // Declare a new variable, and initialize it with a value of 10
    int passengers = 10;
    System.out.println(passengers);

    // Declare a new variable, and initialize it with a value of 
    // a pre-defined variable
    int busTickets = passengers;
    System.out.println(busTickets);
    
    
  }
  
}
