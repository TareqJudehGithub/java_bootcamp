public class Form {

  public static void main(String[] args) {
    // entry point
    /* Char type - character
      - char only stores ONE character.
      - char values uses single quotes. Double quotes are for Strings.
      - char consumes less memory than String.
      - char performs faster than String. So  Use char to store single characters, and 
        String to store longer text.
    */

    char gender = 'F';
    System.out.println("Gender: " + gender);

  }
}