public class WholeNumbers {
  public static void main(String[] args){
    
    /*
    int and long
    - Both int and long store whole numbers.
    - int can store values between the range - 2 billions and 2 billions.
    - long can store big numbers. Range between - 9 quintillion and 9 quintillion.
    - long includes an L at the end of the value.
    */
    int numberOfFans = 5000;
    System.out.print("Hello, ");
    System.out.println("There are " + numberOfFans + " fans at the Quidditch games.");
    
    long population = 7000000000L;
    System.out.println("Population = " + population);
  }
}